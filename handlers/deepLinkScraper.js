  reportData.endId = endId;
  
  console.log('\n' + '='.repeat(40));
  console.log(`\u{1F680} Scraping dimulai...`);
  console.log(`   Channel: ${parsedChannelId}`);
  console.log(`   Range: ID ${startId} - ${endId}`);
