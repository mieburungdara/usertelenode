/**
 * Random delay antara konfigurasi MIN_DELAY-MAX_DELAY
 */
function randomDelay() {
  const minDelay = typeof config.MIN_DELAY === 'number' && !isNaN(config.MIN_DELAY) ? config.MIN_DELAY : 3000;
  const maxDelay = typeof config.MAX_DELAY === 'number' && !isNaN(config.MAX_DELAY) ? config.MAX_DELAY : 10000;
  const effectiveMin = Math.min(minDelay, maxDelay);
  const effectiveMax = Math.max(minDelay, maxDelay);
  const delay = Math.floor(Math.random() * (effectiveMax - effectiveMin + 1)) + effectiveMin;
  return new Promise((resolve) => setTimeout(() => resolve(delay), delay));
}

/**
 * Cek apakah pesan berisi media (foto/video)
 */
function hasMedia(message) {
  if (!message) return false;
  
  if (message.media) {
    const mediaType = message.media.className;
    return (
      mediaType === 'MessageMediaPhoto' ||
      mediaType === 'MessageMediaDocument' ||
      mediaType === 'MessageMediaVideo' ||
      mediaType === 'MessageMediaWebPage' ||
      mediaType === 'MessageMediaGeo' ||
      mediaType === 'MessageMediaContact' ||
      mediaType === 'MessageMediaPoll'
    );
  }
  return false;
}

/**
 * Escape HTML special characters to prevent HTML injection in report
 * Also escape markdown special characters for safe display
 */
function escapeForReport(str) {
  if (!str) return '';
  let escaped = String(str).replace(/[*_\[\]()~>#+\-=|{}.!]/g, '\\$&');
  escaped = escaped
    .replace(/&/g, '\u0026amp;')
    .replace(/</g, '\u0026lt;')
    .replace(/>/g, '\u0026gt;')
    .replace(/"/g, '\u0026quot;')
    .replace(/`/g, '\\`');
  return escaped;
}

